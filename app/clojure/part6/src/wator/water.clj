(ns wator.water
  "Wa-Tor シミュレーション - 水

   空のセル（水）を表します。"
  (:require [wator.cell :as cell]))

;; =============================================================================
;; コンストラクタ
;; =============================================================================

(defn make
  "水セルを作成"
  []
  {::cell/type ::water})

;; =============================================================================
;; 判定関数
;; =============================================================================

(defn is?
  "水セルかどうか判定"
  [cell]
  (= ::water (::cell/type cell)))

;; =============================================================================
;; マルチメソッド実装
;; =============================================================================

(defmethod cell/tick ::water
  [_water _loc _world]
  nil)

(defmethod cell/display ::water
  [_water]
  ".")
